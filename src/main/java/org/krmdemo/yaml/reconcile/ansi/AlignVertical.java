package org.krmdemo.yaml.reconcile.ansi;

/**
 * Vertical alignment of rendered content.
 */
public enum AlignVertical {

    /**
     * The content of block is vertically aligned to the top.
     */
    TOP,

    /**
     * The content of block is vertically aligned to the middle.
     */
    MIDDLE,

    /**
     * The content of block is vertically aligned to the bottom.
     */
    BOTTOM
}
