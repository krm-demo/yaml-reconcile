package org.krmdemo.yaml.reconcile.ansi;

/**
 * Horizontal alignment of rendered content.
 */
public enum AlignHorizontal {

    /**
     * The content of block is horizontally aligned to the left.
     */
    LEFT,

    /**
     * The content of block is horizontally aligned to the center.
     */
    CENTER,

    /**
     * The content of block is horizontally aligned to the right.
     */
    RIGHT
}
